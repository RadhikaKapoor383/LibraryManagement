    private boolean passwordValidator(String pass) {
        String passwordRegex = "^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)(?=.*[@$!%*?&])[A-Za-z\\d@$!%*?&]{8,}$";
        Pattern pattern = Pattern.compile(passwordRegex);
        Matcher matcher = pattern.matcher(pass);
        return matcher.matches();
    }

    private boolean usernameValidator(String pass) {
        String usernameRegex = "^[a-zA-Z][a-zA-Z0-9_.]{2,19}$";
        Pattern pattern = Pattern.compile(usernameRegex);
        Matcher matcher = pattern.matcher(username.getText());
        return matcher.matches();
    }

    private boolean emailValidator(String pass) {
        String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@gmail.com";
        Pattern pattern = Pattern.compile(emailRegex);
        Matcher matcher = pattern.matcher(email.getText());
        return matcher.matches();
    }

    private boolean numberValidator(String pass) {
        String regex = "^03\\d{2}-?\\d{7}$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(contactNum.getText());
        return matcher.matches();
    }

    private boolean cnicValidator(String pass) {
        String regex = "^\\d{5}-?\\d{6}-?\\d{1}$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(cnic.getText());
        return matcher.matches();
    }
    

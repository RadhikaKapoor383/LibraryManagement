    private void ViewAvailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewAvailActionPerformed
        ViewingBooks viewCars = new ViewingBooks();
        viewCars.NameLabel2.setText(NameLabel2.getText());
        viewCars.IDLabel2.setText(IDLabel2.getText());
        String URL = "jdbc:mysql://localhost:3306/GoodRead";
        String username = "root";
        String password = "Radhika1@";
        String query = "Select * from books where available=true";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
        } catch (ClassNotFoundException e) {
            System.out.println(e.getMessage());
        }

        try {
            Connection connect = DriverManager.getConnection(URL, username, password);

            PreparedStatement stmt = connect.prepareStatement(query);

            ResultSet rs = stmt.executeQuery();

            DefaultTableModel model = (DefaultTableModel) viewCars.booksTable.getModel();

            while (rs.next()) {
                
                String BooksTitle = "Book Title: " + rs.getString("BookTitle") + "<br>" ;
                String specifications = "ID: " + rs.getString("BookID") + "<br>"
                + "Author Name: " + rs.getString("Author") + "<br>"
                + "Publisher Name: " + rs.getString("Publisher") + "<br>"
                + "ISBN NO.: " + rs.getString("ISBN") + "<br>"
                + "Year: " + rs.getInt("Year") + "<br>"
                + "Number Of books: " + rs.getInt("NumberOfBooks") + "<br>"
                + "Status: " + rs.getString("Status");

                Object[] rowData = {BooksTitle + "<html>" + specifications + "</html>"};
                model.addRow(rowData);
            }

            viewCars.show();
            dispose();

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_ViewAvailActionPerformed


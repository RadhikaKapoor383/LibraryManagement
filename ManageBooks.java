    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        EditBooks editbook = new EditBooks();
        String ID = JOptionPane.showInputDialog("Please enter the ID of book you want to edit :");
        String bookID = ID;
        String URL = "jdbc:mysql://localhost:3306/GoodRead";
        String username = "root";
        String password = "Radhika1@";
        String query = "select * from books where BookID = ?";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
        } catch (ClassNotFoundException e) {
            System.out.println(e.getMessage());
        }

        try (java.sql.Connection connect = DriverManager.getConnection(URL, username, password);
         PreparedStatement stmt = connect.prepareStatement(query)){
            stmt.setString(1, bookID);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                editbook.Title.setText(rs.getString("BookTitle"));
                editbook.AuthorName.setText(rs.getString("Author"));
                editbook.PublisherName.setText(rs.getString("Publisher"));
                editbook.Year.setText(rs.getString("Year"));
                editbook.noOfbook.setText(rs.getString("NumberOfBooks"));
                editbook.isbn.setText(rs.getString("ISBN"));
                editbook.status.setText("" + rs.getString("Status"));
                editbook.bookID.setText("" + bookID);
                editbook.ID.setText("" + rs.getInt("sno"));

                editbook.show();
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "The book does not exist.");
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        editbook.show();
        dispose();
    }//GEN-LAST:event_EditActionPerformed

    private void RemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveActionPerformed
        String ID = JOptionPane.showInputDialog("Please enter the ID of user you want to remove!");
        int bookID = Integer.parseInt(ID);
        String URL = "jdbc:mysql://localhost:3306/GoodRead";
        String username = "root";
        String password = "Radhika1@";
        String query = "select * from books where BookID = ?";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
        } catch (ClassNotFoundException e) {
            System.out.println(e.getMessage());
        }

        try (java.sql.Connection connect = DriverManager.getConnection(URL, username, password);
         PreparedStatement stmt = connect.prepareStatement(query)) {
            stmt.setInt(1, bookID);
            Boolean availability = false;
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                availability = rs.getBoolean("Availability");
            }

            if (availability) {
                JOptionPane.showMessageDialog(this, "Could not delete the given book as it is borrowed");

            } else {
                PreparedStatement stmt2;
                stmt2 = connect.prepareStatement("Delete from books where BookID = ?");
                stmt2.setInt(1, bookID);
                int rows = stmt2.executeUpdate();
                if (rows > 0) {
                    JOptionPane.showMessageDialog(this, "Book of ID " + bookID + " has been successfully removed.");
                } else {
                    JOptionPane.showMessageDialog(this, "The book does not exist");
                }
            }

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_RemoveActionPerformed

